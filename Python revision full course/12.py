l1 = [1, 8, 76, 98, 34]
print(l1)

#l1.sort --> ascending order
l1.sort()
print(l1)

# l1.reverse
l1.reverse()
print(l1)

#l1.append
l1.append(45)
print(l1)
l1.append(56)
print(l1)

#l1.insert
l1.insert(2, 544) #adds 544 at index 2
print(l1)

#l1.pop
l1.pop(2) #removes element at index 2
print(l1)

# l1.remove
l1.remove(8) #removes 21 from list
print(l1)
